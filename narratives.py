def inject_user_tokens(template: str, user_tokens: dict, income: float = 0.0,
                       year: int = 0, age: int = 29) -> str:
    """
    将用户原词和模拟数值回注到模板中。

    Args:
        template: 含占位符的模板字符串
        user_tokens: {'user_value_keyword': '自由', 'user_raw_concern': '女儿', ...}
        income: 该年收入（万元），用于 {income} 占位符
        year: 时间点（1/3/5/10）
        age: 用户当前年龄

    Returns:
        回注后的文本
    """
    # 默认兜底
    defaults = {
        "user_value_keyword": "你在乎的那件事",
        "user_raw_concern": "家人",
        "user_raw_phrase": "你说过的话",
    }
    # XSS 防护：所有用户来源 token 一律 html.escape，防止恶意输入（<script>等）
    # 注入到 unsafe_allow_html=True 的 st.markdown 中触发脚本执行
    raw_tokens = {**defaults, **(user_tokens or {})}
    safe_tokens = {k: _html.escape(str(v), quote=True) for k, v in raw_tokens.items()}

    # 数值兜底
    income_display = f"{income:.1f} 万" if income < 100 else f"{income/100:.2f} 百万"
    replacements = {
        **safe_tokens,
        "income": income_display,
        "year": year,
        "age": age + year,
    }

    try:
        return template.format(**replacements)
    except (KeyError, IndexError):
        # 任何占位符缺失时，返回原模板
        return template


def get_narrative(option_id: str, value_dim: str, outcome: str, year: int,
                  user_tokens: dict, income: float, age: int = 29) -> str:
    """
    三维索引查询 + 兜底 fallback + 原词回注

    Args:
        option_id: 'stay_soe' | 'entrepreneur'
        value_dim: 'wealth' | 'freedom' | 'achievement' | 'relation' | 'meaning'
        outcome: 'optimistic' | 'baseline' | 'pessimistic'
        year: 1 | 3 | 5 | 10
    """
    # Level 1：精确匹配
    try:
        tpl = NARRATIVES[option_id][value_dim][outcome][year]
        return inject_user_tokens(tpl, user_tokens, income, year, age)
    except (KeyError, TypeError):
        pass

    # Level 2：同选项，降级到 baseline
    try:
        tpl = NARRATIVES[option_id][value_dim]["baseline"][year]
        return inject_user_tokens(tpl, user_tokens, income, year, age)
    except (KeyError, TypeError):
        pass

    # Level 3：同选项，降级到 wealth 维度
    try:
        tpl = NARRATIVES[option_id]["wealth"][outcome][year]
        return inject_user_tokens(tpl, user_tokens, income, year, age)
    except (KeyError, TypeError):
        pass

    # Level 4：最终兜底
    tpl = FALLBACK_TEMPLATES[option_id][year]
    return inject_user_tokens(tpl, user_tokens, income, year, age)


def get_punchline(option_id: str, outcome: str, user_tokens: dict) -> str:
    """取扎心总结文案 + 原词回注"""
    tpl = PUNCHLINES.get(option_id, {}).get(outcome, PUNCHLINES[option_id]["baseline"])
    return inject_user_tokens(tpl, user_tokens)


